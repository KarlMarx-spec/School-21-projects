#include "libft.h"
#include <unistd.h>

static void	ft_check(int n, int fd)
{
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else if (n == 0)
		ft_putchar_fd('0', fd);
}

void		ft_putnbr_fd(int n, int fd)
{
	char	s[10];
	int		i;

	if (fd < 0)
		return ;
	if (n == 0 || n == -2147483648)
	{
		ft_check(n, fd);
		return ;
	}
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	i = 0;
	while (n > 0)
	{
		s[i] = n % 10 + '0';
		n = n / 10;
		i++;
	}
	i--;
	while (i >= 0)
		ft_putchar_fd(s[i--], fd);
}
