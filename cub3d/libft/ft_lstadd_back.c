#include "libft.h"
#include <stdlib.h>

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*st;

	if (new)
	{
		st = (*lst);
		if (st == NULL)
			(*lst) = new;
		else
		{
			while (st->next)
				st = st->next;
			st->next = new;
		}
	}
}
